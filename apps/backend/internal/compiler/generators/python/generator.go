func (g *Generator) generateConfigYaml(wf *workflow.Workflow) []compiler.GeneratedFile {
	// Generate config from workflow variables
	var vars []string
	for k, v := range wf.Variables {
		vars = append(vars, fmt.Sprintf("# %s: %v", k, v))
	}

	content := fmt.Sprintf(`# AIStudio Generated Configuration
# Workflow: %s (%s)

workflow:
  id: %s
  name: %s
  version: %d

# Workflow Variables
%s
`, wf.Name, wf.ID, wf.ID, wf.Name, wf.Version, strings.Join(vars, "\n"))

	return []compiler.GeneratedFile{
		{Path: "config/config.yaml", Content: content, Mode: 0644},
	}
}

