// Package python generates complete Python projects from workflows.
//
// The Python Generator produces a standard Python project structure:
//
//	project-name/
//	├── .aistudio/
//	│   └── workflow.json
//	├── src/
//	│   ├── __init__.py
//	│   ├── data_loader.py
//	│   ├── model.py
//	│   ├── train.py
//	│   └── utils.py
//	├── config/
//	│   └── config.yaml
//	├── data/
//	├── models/
//	├── outputs/
//	├── tests/
//	│   └── __init__.py
//	├── requirements.txt
//	├── setup.py
//	├── main.py
//	├── README.md
//	└── .gitignore
//
// The generated project is a real, runnable Python project that can
// be executed independently of AIStudio.
package python

