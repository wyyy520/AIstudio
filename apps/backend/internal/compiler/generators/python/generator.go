func (g *Generator) collectPackages(wf *workflow.Workflow) []string {
	// Collect Python packages based on node types
	pkgs := []string{
		"pyyaml>=6.0",
	}

	for _, node := range wf.Nodes {
		switch node.Type {
		case workflow.NodeTypeModelTrainer:
			pkgs = append(pkgs, "torch>=2.0.0")
			pkgs = append(pkgs, "torchvision>=0.15.0")
		case workflow.NodeTypeModelInference:
			pkgs = append(pkgs, "torch>=2.0.0")
		case workflow.NodeTypeDataLoader:
			pkgs = append(pkgs, "numpy>=1.24.0")
		case workflow.NodeTypeDataPreprocess:
			pkgs = append(pkgs, "numpy>=1.24.0")
			pkgs = append(pkgs, "scikit-learn>=1.3.0")
		case workflow.NodeTypeVisualization:
			pkgs = append(pkgs, "matplotlib>=3.7.0")
		case workflow.NodeTypeHyperparamTune:
			pkgs = append(pkgs, "optuna>=3.0.0")
		}
	}

	return uniqueStrings(pkgs)
}

func (g *Generator) requiresGPU(wf *workflow.Workflow) bool {
	for _, node := range wf.Nodes {
		if node.Type == workflow.NodeTypeModelTrainer {
			if device, ok := node.Config["device"]; ok {
				if deviceStr, ok := device.(string); ok && deviceStr == "cuda" {
					return true
				}
			}
		}
	}
	return false
}

func sanitizeName(name string) string {
	name = strings.ToLower(name)
	name = strings.ReplaceAll(name, " ", "_")
	name = strings.ReplaceAll(name, "-", "_")
	// Remove non-alphanumeric characters
	var result []rune
	for _, r := range name {
		if (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9') || r == '_' {
			result = append(result, r)
		}
	}
	return string(result)
}

func sanitizeFuncName(name string) string {
	name = strings.Title(sanitizeName(name))
	return name
}

func uniqueStrings(s []string) []string {
	seen := make(map[string]bool)
	var result []string
	for _, v := range s {
		if !seen[v] {
			seen[v] = true
			result = append(result, v)
		}
	}
	return result
}

