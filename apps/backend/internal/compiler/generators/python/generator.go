// Generator generates Python projects from workflows.
type Generator struct {
	compiler.BaseGenerator
}

// NewGenerator creates a new Python Generator.
func NewGenerator() *Generator {
	return &Generator{
		BaseGenerator: compiler.BaseGenerator{
			TargetID:      workflow.TargetPython,
			GeneratorName: "Python Project Generator",
			GeneratorDesc: "Generates standard Python projects with proper structure, dependencies, and entry points",
			GeneratorVer:  "2.0.0",
		},
	}
}

func (g *Generator) Validate(wf *workflow.Workflow) error {
	return g.DefaultValidate(wf)
}

func (g *Generator) EstimateResources(wf *workflow.Workflow) (*compiler.ResourceEstimate, error) {
	return g.DefaultEstimateResources(wf)
}

func (g *Generator) CompileTimeValidate(ctx context.Context) error {
	return g.DefaultCompileTimeValidate(ctx)
}

func (g *Generator) RuntimeRequirement(wf *workflow.Workflow) (*compiler.RuntimeRequirement, error) {
	pkgs := g.collectPackages(wf)
	return &compiler.RuntimeRequirement{
		Name:     "python",
		Version:  "3.9+",
		Python:   ">=3.9",
		Packages: pkgs,
		Commands: []string{"python", "pip"},
		GPU:      g.requiresGPU(wf),
	}, nil
}

func (g *Generator) Generate(ctx context.Context, wf *workflow.Workflow, opts compiler.CompileOptions) (*compiler.GenerateResult, error) {
	// Determine project name
	projectName := opts.ProjectName
	if projectName == "" {
		projectName = sanitizeName(wf.Name)
	}

	// Determine output directory
	outputDir := opts.OutputDir
	if outputDir == "" {
		outputDir = filepath.Join(".", projectName)
	}

	// Check if output directory exists
	if !opts.Force {
		if _, err := os.Stat(outputDir); err == nil {
			return nil, fmt.Errorf("output directory already exists: %s (use Force=true to overwrite)", outputDir)
		}
	}

	// Create project structure
	if err := os.MkdirAll(outputDir, 0755); err != nil {
		return nil, fmt.Errorf("failed to create output directory: %w", err)
	}

	var files []compiler.GeneratedFile

	// Generate files
	files = append(files, g.generateMainPy(wf, projectName)...)
	files = append(files, g.generateRequirements(wf)...)
	files = append(files, g.generateSetupPy(wf, projectName)...)
	files = append(files, g.generateConfigYaml(wf)...)
	files = append(files, g.generateGitignore()...)
	files = append(files, g.generateReadme(wf, projectName)...)
	files = append(files, g.generateSourceFiles(wf)...)
	files = append(files, g.generateTestFiles()...)
	files = append(files, g.generateWorkflowCopy(wf)...)

	// Write files to disk
	entryPoints := []string{filepath.Join(outputDir, "main.py")}
	for _, f := range files {
		fullPath := filepath.Join(outputDir, f.Path)
		dir := filepath.Dir(fullPath)
		if err := os.MkdirAll(dir, 0755); err != nil {
			return nil, fmt.Errorf("failed to create directory %s: %w", dir, err)
		}
		if err := os.WriteFile(fullPath, []byte(f.Content), os.FileMode(f.Mode)); err != nil {
			return nil, fmt.Errorf("failed to write file %s: %w", f.Path, err)
		}
	}

	return &compiler.GenerateResult{
		Target:      workflow.TargetPython,
		ProjectRoot: outputDir,
		EntryPoints: entryPoints,
		Files:       files,
		ProjectName: projectName,
	}, nil
}

